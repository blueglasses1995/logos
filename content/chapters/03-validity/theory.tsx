export function TheoryContent() {
  return (
    <article className="prose prose-zinc max-w-none">
      <h1>第3章: 論証の妥当性と健全性</h1>

      <h2>論証とは</h2>
      <p>
        <strong>論証（argument）</strong>とは、一つ以上の<strong>前提（premise）</strong>から
        <strong>結論（conclusion）</strong>を導く推論の構造です。
      </p>
      <blockquote>
        <p>前提1: すべての哺乳類は温血動物である</p>
        <p>前提2: クジラは哺乳類である</p>
        <p>結論: ゆえに、クジラは温血動物である</p>
      </blockquote>

      <h2>妥当性（Validity）</h2>
      <p>
        論証が<strong>妥当（valid）</strong>であるとは、前提がすべて真であると仮定した場合に
        結論が必ず真になることです。
        つまり「前提が真で結論が偽」という組み合わせが不可能であることです。
      </p>
      <p>
        重要: 妥当性は論証の<strong>形式</strong>についての性質です。
        前提や結論の内容が実際に真かどうかは問いません。
      </p>
      <blockquote>
        <p>前提1: すべての魚は空を飛べる（偽）</p>
        <p>前提2: サメは魚である（真）</p>
        <p>結論: ゆえに、サメは空を飛べる（偽）</p>
      </blockquote>
      <p>
        この論証は前提1が偽ですが、形式としては妥当です。
        「前提がすべて真なら結論も真」という条件を満たしています
        （前提がすべて真になることがないため、空真として成立）。
      </p>

      <h2>健全性（Soundness）</h2>
      <p>
        論証が<strong>健全（sound）</strong>であるとは、
        (1) 論証が妥当であり、かつ (2) すべての前提が実際に真であることです。
        健全な論証の結論は必ず真です。
      </p>

      <h2>主要な推論規則</h2>

      <h3>モーダスポネンス（肯定式）</h3>
      <p>P → Q, P ∴ Q</p>
      <p>「PならばQ」が成り立ち、Pが真なら、Qも真です。</p>

      <h3>モーダストレンス（否定式）</h3>
      <p>P → Q, ¬Q ∴ ¬P</p>
      <p>「PならばQ」が成り立ち、Qが偽なら、Pも偽です。（対偶の応用）</p>

      <h3>仮言三段論法</h3>
      <p>P → Q, Q → R ∴ P → R</p>
      <p>条件文を連鎖させることで、新しい条件文を導けます。</p>

      <h3>選言三段論法</h3>
      <p>P ∨ Q, ¬P ∴ Q</p>
      <p>「PまたはQ」が成り立ち、Pが偽なら、Qが真です。</p>

      <h2>反例による妥当性の否定</h2>
      <p>
        論証が妥当でないことを示すには、
        <strong>前提がすべて真で結論が偽</strong>になる具体例（反例）を一つ見つければ十分です。
      </p>
      <p>
        例: 「P → Q, Q ∴ P」（後件肯定の誤謬）の反例:
        P = 偽, Q = 真のとき、P → Qは真、Qは真、しかしPは偽。
      </p>
    </article>
  )
}
